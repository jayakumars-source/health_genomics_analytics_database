CREATE TABLE insurance_policy (
    policy_id INT AUTO_INCREMENT,
    patient_id INT,
    policy_type VARCHAR(100),
    coverage_amount DECIMAL(12 , 2 ),
    premium_amount DECIMAL(10 , 2 ),
    policy_start_date DATE,
    policy_end_date DATE,
    genomic_risk_score DECIMAL(5 , 2 ),
    policy_status VARCHAR(50),
    CONSTRAINT ip_policy_id_pk PRIMARY KEY (policy_id),
    CONSTRAINT ip_patient_id_fk FOREIGN KEY (patient_id)
        REFERENCES patients (patient_id),
    CONSTRAINT ip_policy_type_ck CHECK (policy_type IN ('Basic' , 'Premium', 'Comprehensive')),
    CONSTRAINT ip_policy_status_ck CHECK (policy_status IN ('Active' , 'Expired', 'cancelled', 'Claimed')),
    CONSTRAINT ip_genomic_risk_score_ck CHECK (genomic_risk_score BETWEEN 0 AND 100)
);


