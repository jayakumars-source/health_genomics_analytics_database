CREATE TABLE variants (
    variant_id INT AUTO_INCREMENT,
    patient_id INT,
    gene_id INT,
    chromosome VARCHAR(5) NOT NULL,
    position BIGINT,
    reference_allele VARCHAR(15),
    alternate_allele VARCHAR(15),
    mutation_type VARCHAR(100),
