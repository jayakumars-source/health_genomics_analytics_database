CREATE TABLE cancer_diagnosis (
    diagnosis_id INT AUTO_INCREMENT,
    patient_id INT,
    cancer_id INT,
    stage VARCHAR(5),
    tumor_size DECIMAL(5 , 2 ),
    metastasis VARCHAR(5),
    diagnosis_date DATE,
    oncologist_name VARCHAR(100),
    treatment_plan VARCHAR(250),
    CONSTRAINT cd_diagnosis_id_pk PRIMARY KEY (diagnosis_id),
    CONSTRAINT cd_patient_id_fk FOREIGN KEY (patient_id)
        REFERENCES patients (patient_id),
    CONSTRAINT cd_cancer_id_fk FOREIGN KEY (cancer_id)
        REFERENCES cancers (cancer_id),
    CONSTRAINT cd_metastasis_ck CHECK (metastasis IN ('Yes' , 'No')),
    CONSTRAINT cd_stage_ck CHECK (stage IN ('0' , 'I', 'II', 'III', 'IV'))
);

